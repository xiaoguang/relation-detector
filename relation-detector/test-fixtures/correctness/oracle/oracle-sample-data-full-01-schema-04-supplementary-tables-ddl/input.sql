-- ============================================================
-- ERP系统补充表: 发货物流、销售提成、促销活动、
--   三单匹配、固定资产、BOM生产工单、客服工单
-- 关系说明:
--   shipments -> sales_orders (1:1), 通过tracking_no追踪物流
--   sales_commissions -> sales_orders + employees (N:1:1), 按销售额计算提成
--   promotions -> sales_order_items (N:M), 通过promotion_items关联
--   invoices -> purchase_orders + purchase_receipts (三单匹配)
--   fixed_assets + depreciation_log (固定资产+折旧)
--   boms -> products (自引用), 生产工单用料展开
--   work_orders -> boms + products (生产工单)
-- ============================================================

-- ============================================================
-- Custom ENUM types
-- ============================================================

-- ============================================================
-- 1. 发货物流模块
-- shipments: 发货单 -> 关联sales_orders
-- shipping_tracks: 物流轨迹 -> 关联shipments
-- ============================================================

CREATE TABLE shipments (
    id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_no VARCHAR2(30) NOT NULL UNIQUE,
    order_id NUMBER(19) CHECK (order_id >= 0) NOT NULL,
    warehouse_id NUMBER(19) CHECK (warehouse_id >= 0) NOT NULL,
    carrier VARCHAR2(100) NOT NULL,
    tracking_no VARCHAR2(50) NULL,
    shipping_method VARCHAR2(40) DEFAULT 'express',
    shipping_fee NUMBER(12,2) DEFAULT 0.00,
    package_count NUMBER(10) DEFAULT 1,
    weight_kg NUMBER(8,3) DEFAULT 0.000,
    status VARCHAR2(40) DEFAULT 'pending',
    picker_id NUMBER(19) NULL,
    packer_id NUMBER(19) NULL,
    shipped_at TIMESTAMP(0) NULL,
    delivered_at TIMESTAMP(0) NULL,
    estimated_delivery_date DATE NULL,
    actual_delivery_date DATE NULL,
    from_address VARCHAR2(300) NULL,
    to_address VARCHAR2(300) NOT NULL,
    receiver_name VARCHAR2(50) NULL,
    receiver_phone VARCHAR2(20) NULL,
    remark VARCHAR2(500) NULL,
    created_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_ship_order FOREIGN KEY (order_id) REFERENCES sales_orders(id),
    CONSTRAINT fk_ship_wh FOREIGN KEY (warehouse_id) REFERENCES warehouses(id)
);

CREATE INDEX idx_shipments_order ON shipments (order_id);
CREATE INDEX idx_shipments_tracking ON shipments (tracking_no);
CREATE INDEX idx_shipments_status ON shipments (status);
CREATE INDEX idx_shipments_carrier ON shipments (carrier);
CREATE INDEX idx_shipments_shipped_at ON shipments (shipped_at);

CREATE OR REPLACE TRIGGER trg_shipments_updated_at
    BEFORE UPDATE ON shipments
    FOR EACH ROW
BEGIN
    :NEW.updated_at := CURRENT_TIMESTAMP;
END;
/

