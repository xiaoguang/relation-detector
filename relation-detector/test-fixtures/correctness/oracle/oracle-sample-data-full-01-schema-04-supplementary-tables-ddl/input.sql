-- 三单匹配明细
CREATE TABLE three_way_matching (
    id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    invoice_id NUMBER(19) CHECK (invoice_id >= 0) NOT NULL,
    purchase_order_id NUMBER(19) CHECK (purchase_order_id >= 0) NOT NULL,
    purchase_receipt_id NUMBER(19) CHECK (purchase_receipt_id >= 0) NOT NULL,
    product_id NUMBER(19) CHECK (product_id >= 0) NOT NULL,
    po_quantity NUMBER(10) NOT NULL,
    receipt_quantity NUMBER(10) NOT NULL,
    invoice_quantity NUMBER(10) NOT NULL,
    po_price NUMBER(12,2) NOT NULL,
    receipt_price NUMBER(12,2) NOT NULL,
    invoice_price NUMBER(12,2) NOT NULL,
    quantity_match NUMBER(1) GENERATED ALWAYS AS (CASE WHEN po_quantity = receipt_quantity AND receipt_quantity = invoice_quantity THEN 1 ELSE 0 END) VIRTUAL,
    price_match NUMBER(1) GENERATED ALWAYS AS (CASE WHEN po_price = receipt_price AND receipt_price = invoice_price THEN 1 ELSE 0 END) VIRTUAL,
    match_status VARCHAR2(40) DEFAULT 'pending',
    match_result VARCHAR2(500) NULL,
    matched_by NUMBER(19) NULL,
    matched_at TIMESTAMP(0) NULL,
    CONSTRAINT fk_twm_invoice FOREIGN KEY (invoice_id) REFERENCES invoices(id) ON DELETE CASCADE,
    CONSTRAINT fk_twm_po FOREIGN KEY (purchase_order_id) REFERENCES purchase_orders(id),
    CONSTRAINT fk_twm_receipt FOREIGN KEY (purchase_receipt_id) REFERENCES purchase_receipts(id),
    CONSTRAINT fk_twm_product FOREIGN KEY (product_id) REFERENCES products(id)
);

CREATE INDEX idx_three_way_matching_invoice ON three_way_matching (invoice_id);
CREATE INDEX idx_three_way_matching_po ON three_way_matching (purchase_order_id);
CREATE INDEX idx_three_way_matching_receipt ON three_way_matching (purchase_receipt_id);

-- ============================================================
-- 5. 固定资产模块
-- 折旧方法: 直线法(straight_line)
-- 折旧公式: 月折旧额 = (原值 - 残值) / 使用月数
-- 净值 = 原值 - 累计折旧
-- ============================================================

CREATE TABLE fixed_assets (
    id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    asset_no VARCHAR2(30) NOT NULL UNIQUE,
    name VARCHAR2(200) NOT NULL,
    category VARCHAR2(40) NOT NULL,
    purchase_date DATE NOT NULL,
    purchase_amount NUMBER(18,2) NOT NULL,
    salvage_value NUMBER(18,2) DEFAULT 0.00,
    useful_life_months NUMBER(10) NOT NULL,
    monthly_depreciation NUMBER(12,2) GENERATED ALWAYS AS (
        ROUND((purchase_amount - salvage_value) / useful_life_months, 2)
    ) VIRTUAL,
    accumulated_depreciation NUMBER(18,2) DEFAULT 0.00,
    net_book_value NUMBER(18,2) GENERATED ALWAYS AS (
        purchase_amount - accumulated_depreciation
    ) VIRTUAL,
    department_id NUMBER(19) CHECK (department_id >= 0) NOT NULL,
    custodian_id NUMBER(19) NULL,
    location VARCHAR2(200) NULL,
    status VARCHAR2(40) DEFAULT 'in_use',
    last_depreciation_date DATE NULL,
    created_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_fa_dept FOREIGN KEY (department_id) REFERENCES departments(id),
    CONSTRAINT fk_fa_custodian FOREIGN KEY (custodian_id) REFERENCES employees(id) ON DELETE SET NULL
);

CREATE INDEX idx_fixed_assets_dept ON fixed_assets (department_id);
CREATE INDEX idx_fixed_assets_category ON fixed_assets (category);
CREATE INDEX idx_fixed_assets_status ON fixed_assets (status);

CREATE OR REPLACE TRIGGER trg_fixed_assets_updated_at
    BEFORE UPDATE ON fixed_assets
    FOR EACH ROW
BEGIN
    :NEW.updated_at := CURRENT_TIMESTAMP;
END;
/

