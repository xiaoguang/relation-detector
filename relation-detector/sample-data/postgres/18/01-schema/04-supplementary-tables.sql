    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parent_product_id BIGINT CHECK (parent_product_id >= 0) NOT NULL,
    child_product_id BIGINT CHECK (child_product_id >= 0) NOT NULL,
    quantity DECIMAL(10,3) NOT NULL,
    unit VARCHAR(20) NOT NULL,
    scrap_rate DECIMAL(5,4) DEFAULT 0.00,
    sort_order INTEGER DEFAULT 0,
    effective_date DATE NOT NULL,
    expiry_date DATE NULL,
    status bom_status_type DEFAULT 'active',
    created_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_parent_child UNIQUE (parent_product_id, child_product_id, effective_date),
    CONSTRAINT fk_bom_parent FOREIGN KEY (parent_product_id) REFERENCES products(id) ON DELETE CASCADE,
    CONSTRAINT fk_bom_child FOREIGN KEY (child_product_id) REFERENCES products(id) ON DELETE CASCADE
);

CREATE INDEX idx_boms_child ON boms (child_product_id);
CREATE INDEX idx_boms_status ON boms (status);

