-- ============================================================
-- 1. 发货物流模块
-- shipments: 发货单 -> 关联sales_orders
-- shipping_tracks: 物流轨迹 -> 关联shipments
-- ============================================================

CREATE TABLE shipments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_no VARCHAR(30) NOT NULL UNIQUE,
    order_id BIGINT CHECK (order_id >= 0) NOT NULL,
    warehouse_id BIGINT CHECK (warehouse_id >= 0) NOT NULL,
    carrier VARCHAR(100) NOT NULL,
    tracking_no VARCHAR(50) NULL,
    shipping_method shipping_method_type DEFAULT 'express',
    shipping_fee DECIMAL(12,2) DEFAULT 0.00,
    package_count INTEGER DEFAULT 1,
    weight_kg DECIMAL(8,3) DEFAULT 0.000,
    status shipment_status_type DEFAULT 'pending',
    picker_id BIGINT NULL,
    packer_id BIGINT NULL,
    shipped_at TIMESTAMP(0) NULL,
    delivered_at TIMESTAMP(0) NULL,
    estimated_delivery_date DATE NULL,
    actual_delivery_date DATE NULL,
    from_address VARCHAR(300) NULL,
    to_address VARCHAR(300) NOT NULL,
    receiver_name VARCHAR(50) NULL,
    receiver_phone VARCHAR(20) NULL,
    remark VARCHAR(500) NULL,
    created_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_ship_order FOREIGN KEY (order_id) REFERENCES sales_orders(id),
    CONSTRAINT fk_ship_wh FOREIGN KEY (warehouse_id) REFERENCES warehouses(id)
);

CREATE INDEX idx_shipments_order ON shipments (order_id);
CREATE INDEX idx_shipments_tracking ON shipments (tracking_no);
CREATE INDEX idx_shipments_status ON shipments (status);
CREATE INDEX idx_shipments_carrier ON shipments (carrier);
CREATE INDEX idx_shipments_shipped_at ON shipments (shipped_at);

CREATE TRIGGER trg_shipments_updated_at
    BEFORE UPDATE ON shipments
    FOR EACH ROW
    EXECUTE FUNCTION update_timestamp();

CREATE TABLE shipping_tracks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    shipment_id BIGINT CHECK (shipment_id >= 0) NOT NULL,
    track_time TIMESTAMP(0) NOT NULL,
    location VARCHAR(200) NOT NULL,
    status_desc VARCHAR(200) NOT NULL,
    operator VARCHAR(50) NULL,
    remark VARCHAR(200) NULL,
    created_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_st_shipment FOREIGN KEY (shipment_id) REFERENCES shipments(id) ON DELETE CASCADE
);

CREATE INDEX idx_shipping_tracks_shipment ON shipping_tracks (shipment_id);
CREATE INDEX idx_shipping_tracks_track_time ON shipping_tracks (track_time);

