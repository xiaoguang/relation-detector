-- 5. 固定资产模块
-- 折旧方法: 直线法(straight_line)
-- 折旧公式: 月折旧额 = (原值 - 残值) / 使用月数
-- 净值 = 原值 - 累计折旧
-- ============================================================

CREATE TABLE fixed_assets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    asset_no VARCHAR(30) NOT NULL UNIQUE,
    name VARCHAR(200) NOT NULL,
    category asset_category_type NOT NULL,
    purchase_date DATE NOT NULL,
    purchase_amount DECIMAL(18,2) NOT NULL,
    salvage_value DECIMAL(18,2) DEFAULT 0.00,
    useful_life_months INTEGER NOT NULL,
    monthly_depreciation DECIMAL(12,2) GENERATED ALWAYS AS (
        ROUND((purchase_amount - salvage_value) / useful_life_months, 2)
    ) STORED,
    accumulated_depreciation DECIMAL(18,2) DEFAULT 0.00,
    net_book_value DECIMAL(18,2) GENERATED ALWAYS AS (
        purchase_amount - accumulated_depreciation
    ) STORED,
    department_id BIGINT CHECK (department_id >= 0) NOT NULL,
    custodian_id BIGINT NULL,
    location VARCHAR(200) NULL,
    status asset_status_type DEFAULT 'in_use',
    last_depreciation_date DATE NULL,
    created_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_fa_dept FOREIGN KEY (department_id) REFERENCES departments(id),
    CONSTRAINT fk_fa_custodian FOREIGN KEY (custodian_id) REFERENCES employees(id) ON DELETE SET NULL
);

CREATE INDEX idx_fixed_assets_dept ON fixed_assets (department_id);
CREATE INDEX idx_fixed_assets_category ON fixed_assets (category);
CREATE INDEX idx_fixed_assets_status ON fixed_assets (status);

CREATE TRIGGER trg_fixed_assets_updated_at
    BEFORE UPDATE ON fixed_assets
    FOR EACH ROW
    EXECUTE FUNCTION update_timestamp();

CREATE TABLE depreciation_log (
