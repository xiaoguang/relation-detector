-- 9. 绩效考核模块
-- 考核周期: 月度/季度/年度
-- 评分维度: 业绩(40%) + 能力(30%) + 态度(20%) + 出勤(10%)
-- ============================================================

CREATE TABLE performance_reviews (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    review_no VARCHAR(30) NOT NULL UNIQUE,
    employee_id BIGINT CHECK (employee_id >= 0) NOT NULL,
    reviewer_id BIGINT CHECK (reviewer_id >= 0) NOT NULL,
    review_period VARCHAR(7) NOT NULL,
    review_type performance_review_type_type NOT NULL,
    -- 评分维度
    performance_score DECIMAL(5,2) DEFAULT 0.00,
    competency_score DECIMAL(5,2) DEFAULT 0.00,
    attitude_score DECIMAL(5,2) DEFAULT 0.00,
    attendance_score DECIMAL(5,2) DEFAULT 0.00,
    -- 加权总分 (业绩40% + 能力30% + 态度20% + 出勤10%)
    total_score DECIMAL(5,2) GENERATED ALWAYS AS (
        ROUND(performance_score * 0.40 + competency_score * 0.30 + attitude_score * 0.20 + attendance_score * 0.10, 2)
    ) STORED,
    grade CHAR(1) GENERATED ALWAYS AS (
        CASE
            WHEN ROUND(performance_score * 0.40 + competency_score * 0.30 + attitude_score * 0.20 + attendance_score * 0.10, 2) >= 90 THEN 'S'
            WHEN ROUND(performance_score * 0.40 + competency_score * 0.30 + attitude_score * 0.20 + attendance_score * 0.10, 2) >= 80 THEN 'A'
            WHEN ROUND(performance_score * 0.40 + competency_score * 0.30 + attitude_score * 0.20 + attendance_score * 0.10, 2) >= 70 THEN 'B'
            WHEN ROUND(performance_score * 0.40 + competency_score * 0.30 + attitude_score * 0.20 + attendance_score * 0.10, 2) >= 60 THEN 'C'
            ELSE 'D'
        END
    ) STORED,
    self_assessment TEXT NULL,
    reviewer_comment TEXT NULL,
    improvement_plan TEXT NULL,
    salary_adjustment DECIMAL(12,2) DEFAULT 0.00,
    promotion_recommendation BOOLEAN DEFAULT FALSE,
    status performance_review_status_type DEFAULT 'draft',
    created_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_performance_reviews_emp_period_type UNIQUE (employee_id, review_period, review_type),
    CONSTRAINT fk_pr_employee FOREIGN KEY (employee_id) REFERENCES employees(id),
    CONSTRAINT fk_pr_reviewer FOREIGN KEY (reviewer_id) REFERENCES employees(id)
);

CREATE INDEX idx_performance_reviews_reviewer ON performance_reviews (reviewer_id);
CREATE INDEX idx_performance_reviews_grade ON performance_reviews (grade);
CREATE INDEX idx_performance_reviews_period ON performance_reviews (review_period);

CREATE TRIGGER trg_performance_reviews_updated_at
    BEFORE UPDATE ON performance_reviews
    FOR EACH ROW
    EXECUTE FUNCTION update_timestamp();

