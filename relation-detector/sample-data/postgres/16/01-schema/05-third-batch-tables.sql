-- 2. 应收账款账龄 (AR Aging)
-- 计算原理: 按账期分桶: 0-30天, 31-60天, 61-90天, 91-180天, 181-365天, >365天
-- 坏账准备: >365天按50%计提, >180天按20%, >90天按5%
-- ============================================================

CREATE TABLE ar_aging_snapshots (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    snapshot_date DATE NOT NULL,
    customer_id BIGINT CHECK (customer_id >= 0) NOT NULL,
    order_id BIGINT CHECK (order_id >= 0) NOT NULL,
    invoice_amount DECIMAL(18,2) NOT NULL,
    paid_amount DECIMAL(18,2) DEFAULT 0.00,
    outstanding_amount DECIMAL(18,2) GENERATED ALWAYS AS (invoice_amount - paid_amount) STORED,
    due_date DATE NOT NULL,
    aging_days INTEGER GENERATED ALWAYS AS (snapshot_date - due_date) STORED,
    aging_bucket VARCHAR(20) GENERATED ALWAYS AS (
        CASE
            WHEN snapshot_date - due_date <= 0 THEN '未到期'
            WHEN snapshot_date - due_date <= 30 THEN '1-30天'
            WHEN snapshot_date - due_date <= 60 THEN '31-60天'
            WHEN snapshot_date - due_date <= 90 THEN '61-90天'
            WHEN snapshot_date - due_date <= 180 THEN '91-180天'
            WHEN snapshot_date - due_date <= 365 THEN '181-365天'
            ELSE '365天以上'
        END
    ) STORED,
    bad_debt_provision DECIMAL(18,2) GENERATED ALWAYS AS (
        ROUND((invoice_amount - paid_amount) *
        CASE
            WHEN snapshot_date - due_date <= 90 THEN 0.00
            WHEN snapshot_date - due_date <= 180 THEN 0.05
            WHEN snapshot_date - due_date <= 365 THEN 0.20
            ELSE 0.50
        END, 2)
    ) STORED,
    last_collection_date DATE NULL,
    collection_notes VARCHAR(500) NULL,
    CONSTRAINT fk_ar_customer FOREIGN KEY (customer_id) REFERENCES customers(id),
    CONSTRAINT fk_ar_order FOREIGN KEY (order_id) REFERENCES sales_orders(id)
);

CREATE INDEX idx_ar_aging_customer ON ar_aging_snapshots (customer_id);
CREATE INDEX idx_ar_aging_bucket ON ar_aging_snapshots (aging_bucket);
CREATE INDEX idx_ar_aging_snapshot_date ON ar_aging_snapshots (snapshot_date);
CREATE INDEX idx_ar_aging_due_date ON ar_aging_snapshots (due_date);

