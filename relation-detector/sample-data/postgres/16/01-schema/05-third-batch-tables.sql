-- 6. 现金流预测
-- 预测来源: 应收款到期日 + 应付款到期日 + 工资发放计划 + 计划收支
-- 按日/周/月汇总预测净现金流
-- ============================================================

CREATE TABLE cash_flow_forecasts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    forecast_date DATE NOT NULL,
    forecast_type forecast_type_type NOT NULL,
    beginning_balance DECIMAL(18,2) DEFAULT 0.00,
    expected_collections DECIMAL(18,2) DEFAULT 0.00,
    expected_payments DECIMAL(18,2) DEFAULT 0.00,
    expected_salary DECIMAL(18,2) DEFAULT 0.00,
    expected_tax DECIMAL(18,2) DEFAULT 0.00,
    other_income DECIMAL(18,2) DEFAULT 0.00,
    other_expense DECIMAL(18,2) DEFAULT 0.00,
    net_cash_flow DECIMAL(18,2) GENERATED ALWAYS AS (
        expected_collections - expected_payments - expected_salary - expected_tax + other_income - other_expense
    ) STORED,
    ending_balance DECIMAL(18,2) GENERATED ALWAYS AS (
        beginning_balance + expected_collections - expected_payments - expected_salary - expected_tax + other_income - other_expense
    ) STORED,
    created_at TIMESTAMP(0) DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_cash_flow_forecast_date_type UNIQUE (forecast_date, forecast_type)
);

CREATE INDEX idx_cash_flow_forecasts_date ON cash_flow_forecasts (forecast_date);

