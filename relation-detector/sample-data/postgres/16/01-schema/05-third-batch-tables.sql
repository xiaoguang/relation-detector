-- 3. 税务管理模块 (增值税)
-- 税率: 13%(一般货物), 9%(农产品/图书), 6%(服务), 0%(出口)
-- 进项税 = 采购发票税额, 销项税 = 销售发票税额
-- 应交增值税 = 销项税 - 进项税
-- ============================================================

CREATE TABLE tax_invoices (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    invoice_no VARCHAR(50) NOT NULL UNIQUE,
    invoice_code VARCHAR(50) NULL,
